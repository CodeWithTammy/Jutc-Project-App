// Definition of the OnboardingInfo class
class OnboardingInfo {
  final String title;         // Title for the onboarding screen
  final String description;   // Description of what the onboarding screen offers
  final String image;         // Image asset path for the onboarding screen

  // Constructor for initializing the class with required parameters
  OnboardingInfo({
    required this.title,      // Required title parameter
    required this.description, // Required description parameter
    required this.image       // Required image parameter
  });
}
